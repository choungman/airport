package beans;

public class AirplainBean {
	private int airplainNumber;
	private String airplainName;
	private String airport;
	private int seat;
	
	public int getAirplainNumber() {
		return airplainNumber;
	}
	public void setAirplainNumber(int airplainNumber) {
		this.airplainNumber = airplainNumber;
	}
	public String getAirport() {
		return airport;
	}
	public void setAirport(String airport) {
		this.airport = airport;
	}
	public int getSeat() {
		return seat;
	}
	public void setSeat(int seat) {
		this.seat = seat;
	}
	public String getAirplainName() {
		return airplainName;
	}
	public void setAirplainName(String airplainName) {
		this.airplainName = airplainName;
	}
}